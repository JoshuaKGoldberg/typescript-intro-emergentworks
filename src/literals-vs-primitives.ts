// Primitive: general basic type in JavaScript/TypeScript (number, string, ...)
// Literal: specific example of a primitive (0, "", "hello world")

// 'let' variables default to the primitive type
let myValue = 1337;

myValue = 9001;
myValue = 42;

myValue = null;
myValue = undefined;

let myStatus: 0 | 1 | 2;

myStatus = 123;

// 'const' variables default to the literal type
const myConstValue = 1337;
